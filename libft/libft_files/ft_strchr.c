#include "../header/libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	value;

	value = c;
	while (*s)
	{
		if (*s == c)
			return ((char *)s);
		s++;
	}
	if (c == '\0')
		return ((char *)s);
	return (NULL);
}
