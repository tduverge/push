#include "../header/libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t			i;

	if (len == 0)
		return ((char *)dst);
	i = 0;
	while (src[i] != '\0' && (i < len))
	{
		dst[i] = src[i];
		i++;
	}
	while (i < len)
		dst[i++] = '\0';
	return ((char *)dst);
}
